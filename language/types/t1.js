// Boolean
var a = false;
console.log(typeof a);

// undefined
var b;
console.log(typeof b);

// number
var c=1;
console.log(typeof c);

// String
var d="hello";
console.log(typeof d);

// object
var e = {};
console.log(typeof e);
e = [1,2,3]; // array is a special type of object
console.log(typeof e);

// function
var f = function(){};
console.log(typeof f);

